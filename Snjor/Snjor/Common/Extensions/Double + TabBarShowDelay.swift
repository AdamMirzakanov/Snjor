extension Double {
  /// Расширение типа `Double`, добавляющее статическое свойство,
  /// представляющее собой временную задержку для отображения панели вкладок,
  /// равную 0.3 секунды. Это значение используется для создания плавных анимаций
  /// появления панели вкладок или при управлении временем задержки отображения.
  static let tabBarShowDelay = 0.3
}
